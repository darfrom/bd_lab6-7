public class Photographer {
    public int id;
    public String name;
    public String surname;
    public String patronymic;
    public int photo_id;

    public Photographer(int id, String name, String surname, String patronymic, int photo_id) {
        this.id = id;
        this.name = name;
        this.surname = surname;
        this.patronymic = patronymic;
        this.photo_id = photo_id;
    }
}
